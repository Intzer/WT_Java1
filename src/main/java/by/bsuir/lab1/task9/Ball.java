package main.java.by.bsuir.lab1.task9;

public class Ball {
    private double weight;
    private String color;

    public Ball(double weight, String color) {
        this.weight = weight;
        this.color = color;
    }

    public double getWeight() {
        return weight;
    }

    public String getColor() {
        return color;
    }
}